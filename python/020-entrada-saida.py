# Código que lê um número e diz se o número é maior ou não que 10

# Este é um comentário
# Quem estiver no lab use raw_input
numero  = input("Digite um número")
numero  = int(numero)
if numero > 10:
	print("Número maior que 10")
else:
	print("Número menor ou igual à 10")
